<?php
/**
 * Yes / No Functions for use in WC option filters.
 *
 * @since 1.8.1
 * @package WC_Calypso_Bridge
 */

/**
 * Always return 'yes'.
 *
 * @return string yes
 */
function wc_calypso_bridge_return_yes() {
	return 'yes';
}

/**
 * Always returns 'no'.
 *
 * @return string no
 */
function wc_calypso_bridge_return_no() {
	return 'no';
}
